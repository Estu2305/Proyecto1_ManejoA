    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:

        try {
            int idServicio = Integer.parseInt(txtServicio.getText().trim());
            String nombre = txtNombre.getText().trim();
            String descripcion = txtDescripcion.getText().trim();
            double costo = Double.parseDouble(txtCosto.getText().trim());

            Ser dao = new Ser();
            dao.agregarServicio(idServicio, nombre, descripcion, costo);

            dao.limpiarCampos(txtServicio, txtNombre, txtDescripcion, txtCosto);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "El ID y el Costo deben ser n√∫meros v√°lidos.",
                    "Error de formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

