    private void btnEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEActionPerformed
        // TODO add your handling code here:
        int fila = TablaRutina.getSelectedRow();
        if (fila >= 0) {
            int idRutina = (int) TablaRutina.getValueAt(fila, 0);
            try {
                int idCliente = Integer.parseInt(txtCli.getText());
                int idEntrenador = Integer.parseInt(txtEntrenador.getText());
                String descripcion = txtDescripcion.getText();

                rutina.editarRutina(idRutina, idCliente, idEntrenador, descripcion);
                rutina.limpiarCampos(txtCli, txtEntrenador, txtDescripcion);
                rutina.listarRutinas(TablaRutina, this.idEntrenador);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "ID Cliente y Entrenador deben ser números");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila de la tabla para editar");
        }
    }//GEN-LAST:event_btnEActionPerformed

    private void btnEliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliActionPerformed
        // TODO add your handling code here:
        int fila = TablaRutina.getSelectedRow();
        if (fila >= 0) {
            int idRutina = (int) TablaRutina.getValueAt(fila, 0);
            int confirm = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar esta rutina?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                rutina.eliminarRutina(idRutina);
                rutina.listarRutinas(TablaRutina, this.idEntrenador);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila de la tabla para eliminar");
        }
    }//GEN-LAST:event_btnEliActionPerformed

