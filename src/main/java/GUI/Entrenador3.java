    private void btnAgreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgreActionPerformed
        // TODO add your handling code here:
        try {
            int idRutina = Integer.parseInt(txtIdRutina.getText());
            int idEjercicio = Integer.parseInt(txtEjercicio.getText()); // txtEjercicio es tu JTextField
            int series = (int) SPS.getValue();
            int repeticiones = (int) SPR.getValue();
            String duracion = FTD.getText();

            rutinaEjercicioDAO.agregarRutinaEjercicio(idRutina, idEjercicio, series, repeticiones, duracion);
            rutinaEjercicioDAO.listarRutinaEjercicios(TablaRE, this.idEntrenador);

            Ruti rutina = new Ruti();
            if (!rutina.rutinaPerteneceEntrenador(idRutina, this.idEntrenador)) {
                JOptionPane.showMessageDialog(null, "La rutina no pertenece al entrenador que inició sesión");
                return; // No continuar con el agregado
            }

            //Obtener el idEntrenador seleccionado del ComboBox
            String seleccionado = CbEquipo.getSelectedItem().toString(); // "3 - Entrenador3"
            int idEquipo = Integer.parseInt(seleccionado.split(" - ")[0]);

            // 3. Insertar relación cliente-entrenador
            Ej.asignarEjercicioEquipo(idRutina, idEjercicio, idEquipo);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Ingresa números válidos para IdRutina y Ejercicio");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnAgreActionPerformed

