    private void btnREActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnREActionPerformed
        // TODO add your handling code here:

        try {
            int idSucursalOrigen = Integer.parseInt(txtSucursal.getText());
            int idEquipo = Integer.parseInt(txtE.getText());
            int cantidad = Integer.parseInt(txtCantidad.getText());

            // Pedimos la sucursal de destino
            String input = JOptionPane.showInputDialog(this,
                    "Ingrese el ID de la sucursal destino:",
                    "Traslado de inventario", JOptionPane.QUESTION_MESSAGE);

            if (input == null) {
                return; // Usuario cancel√≥
            }
            int idSucursalDestino = Integer.parseInt(input);

            Traslado traslado = new Traslado();

            // 1. Verificar stock en sucursal origen
            int stockDisponible = traslado.obtenerStock(idSucursalOrigen, idEquipo);
            if (stockDisponible < cantidad) {
                JOptionPane.showMessageDialog(this,
                        "Stock insuficiente en sucursal origen.\nStock disponible: " + stockDisponible,
                        "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 2. Descontar en origen
            boolean descontado = traslado.descontarInventario(idSucursalOrigen, idEquipo, cantidad);
            if (!descontado) {
                return;
            }

            // 3. Aumentar en destino
            boolean aumentado = traslado.aumentarInventario(idSucursalDestino, idEquipo, cantidad);
            if (!aumentado) {
                // Si no se pudo aumentar, revertir en origen
                traslado.aumentarInventario(idSucursalOrigen, idEquipo, cantidad);
                JOptionPane.showMessageDialog(this,
                        "No se pudo aumentar en destino. Operaci√≥n revertida.",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            JOptionPane.showMessageDialog(this,
                    "Traslado realizado con √©xito.",
                    "√âxito", JOptionPane.INFORMATION_MESSAGE);

            // Refrescar tabla y limpiar campos
            dao.listarInventario(TablaInventario);
            dao.limpiarCampos(txtInventario, txtSucursal, txtE, txtCantidad, CbEstado, chkMantenimiento);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "Sucursal, Equipo y Cantidad deben ser n√∫meros v√°lidos.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnREActionPerformed

