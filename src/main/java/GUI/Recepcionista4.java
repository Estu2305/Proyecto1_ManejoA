    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Asis dao = new Asis();

        String idAsistenciaText = txtAsistencia.getText().trim();
        String idClienteText = txtCliente.getText().trim();
        String idSucursalText = txtSucursal.getText().trim();

        if (idAsistenciaText.isEmpty() || idClienteText.isEmpty() || idSucursalText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int idAsistencia = Integer.parseInt(idAsistenciaText);
            int idCliente = Integer.parseInt(idClienteText);
            int idSucursal = Integer.parseInt(idSucursalText);

            // Obtener fecha y hora del spinner
            Date fechaSeleccionada = (Date) spinnerFechaHora.getValue();
            Timestamp fechaTimestamp = new Timestamp(fechaSeleccionada.getTime());

            dao.editarAsistencia(idAsistencia, idCliente, idSucursal, fechaTimestamp);

            // limpiar campos
            txtAsistencia.setText("");
            txtCliente.setText("");
            txtSucursal.setText("");
            spinnerFechaHora.setValue(new Date());
            txtAsistencia.requestFocus();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Los campos ID deben ser n√∫meros v√°lidos", "Error de formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        String idText = txtAsistencia.getText().trim();

        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa un ID de asistencia.", "Campo vac√≠o", JOptionPane.WARNING_MESSAGE);
            txtAsistencia.requestFocus();
            return;
        }

        try {
            int idAsistencia = Integer.parseInt(idText);

            Asis dao = new Asis();
            dao.eliminarAsistencia(idAsistencia);

            txtAsistencia.setText("");
            txtAsistencia.requestFocus();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un n√∫mero v√°lido.", "Error de formato", JOptionPane.WARNING_MESSAGE);
            txtAsistencia.requestFocus();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Asis dao = new Asis();

        String idClienteText = txtCliente.getText().trim();
        String idSucursalText = txtSucursal.getText().trim();

        if (idClienteText.isEmpty() || idSucursalText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int idCliente = Integer.parseInt(idClienteText);
            int idSucursal = Integer.parseInt(idSucursalText);

            // Obtener fecha/hora del spinner
            Date fechaSeleccionada = (Date) spinnerFechaHora.getValue();
            Timestamp fechaTimestamp = new Timestamp(fechaSeleccionada.getTime());

            // Pasar al m√©todo de la clase Asis
            dao.agregarAsistencia(idCliente, idSucursal, fechaTimestamp);

            // Limpiar campos
            txtAsistencia.setText("");
            txtCliente.setText("");
            txtSucursal.setText("");
            spinnerFechaHora.setValue(new Date()); // resetea al d√≠a y hora actual
            txtCliente.requestFocus();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Los campos ID Cliente e ID Sucursal deben ser n√∫meros v√°lidos", "Error de formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

